package com.sparta.astha;

public class StringConcatenation {

    public void methodStringConcatenation(){
        System.out.println("-----STRING CONCATENATION START-------");
        System.out.println("5" + "6");//56
        System.out.println("a" + "b" + 7);//ab7
        System.out.println(8 + 7 +"a");//15a
        System.out.println("a"+8+7);//a87
        int nine=9;
        String eight="8";
        System.out.println(6 + 7 + eight + nine);//1389


        System.out.println("-----STRING CONCATENATION END-------");
    }
}
